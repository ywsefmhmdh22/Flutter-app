لعلك صادفت امرأة فائقة الجمال تجلس في مكان ما بدون أن تبدي أيًّا من إيماءات ومظاهر الاهتمام بالأشخاص المحيطين، وعندما تقترب منها لتتعرف عليها، تتصرف معك بقسوة رغم أنك تبادرها بكلام لطيف، فما الذي حدث يا ترى؟ بصراحة إنه الاختبار الأول (اختبار قدرة الرجل على السيطرة في المواقف المضطربة والتعامل معها بروية وهدوء أعصاب).

• جميع الرجال الذين يبدون تأثرًا بذلك أو خوفًا لا تملك المرأة نحوهم أي اهتمام، (بالطبع يخدم النساء في هذا الموضوع الخوف الاجتماعي الذي يرتبط بالموقف).

• الرجل الذي يأتي بثقة تامة بالنفس، لا يدع أول رد فعل للمرأة يربكه، لا يعكس تصرفها، يتابع وكأن شيئًا لم يحدث، لا ينكسر عند أول مقاومة تبديها المرأة تجاه محاولته التعرف عليها، سيلاحظ تغيرًا في تصرف المرأة بالكامل تجاهه، وبأنها أصبحت فجأة شخصًا آخر لطيفًا تمامًا. 

على ماذا ترتكز الاختبارات الأنثوية؟

في بداية التعارف تحاول المرأة بطريقتها الأنثوية اختبار قدرة الرجل الذي يرغب في التقرب منها على تلبية احتياجاتها في: الأمان تجاهه (أي أن يبرهن الرجل على أنه ثقة ولا يشكل تهديدًا نحوها). الأمان الاجتماعي (أي أن يبرهن الرجل على أنه في علاقته معها لن يكون هناك ما يُسيء لمنزلتها الاجتماعية أمام الآخرين). الأمان العاطفي (أي أن يبرهن الرجل على أن تقربه نحو الفتاة يكون لأجلها هي، لا لأنه يريد الجنس معها فقط وحسب). الحماية (حاجة المرأة للحماية تنبع من كونها مخلوق ضعيف، تحتاج رجل تستظل بظله، والمرأة عمومًا لا تحب إلا الرجل القوي، الذي يكشف ضعفها إلى جنب قوته، ولا تميل أن تربط إلا بالرجل، الذي تشعر في كنفه بالأمن والأمان).  

الرجل الذي ينجح في اختبارات القدرة على تلبية الاحتياجات السابقة سيدخل المرحلة التالية من الاختبارات، وفي هذه المرحلة تحاول المرأة اختبار قدرة الرجل على تلبية احتياجاتها الأنثوية الأخرى، وهي: الحاجة إلى الأمان المادي، الحاجة إلى المحادثة، الحاجة إلى الغزل، الحاجة إلى الحب، الحاجة إلى الرومانسية، الحاجة إلى الأولاد.

عندما تكتشف المرأة من خلال اختباراتها الأنثوية أن الرجل قادر على تأمين جميع أو معظم احتياجاتها الداخلية السابقة تطمئن إلى أن حياتها ومستقبلها معه سيكون ناجحًا، وعند ذلك ستشعر أنها وجدت الرجل الذي كانت تبحث عنه. 

الحديث هنا عن الاحتياجات الداخلية العامة عند كل النساء، ولكن بعض النساء عندهنَّ تطلعات إلى احتياجات أخرى خاصة، فالمرأة التي تهوى السفر تحاول اختبار الرجل إذا ما كان على استعداد لذلك، والمرأة التي تريد متابعة دراستها تحاول استكشاف موقف الرجل من ذلك، وهناك الكثير من الاحتياجات الخاصة التي تختلف من امرأة إلى أخرى والتي ستحاول المرأة -التي تعرفت عليها- معرفة موقفك منها. 

بالطبع الاختبارات لا تتوقف عند استكشاف مدى جاهزية الرجل لتلبية احتياجات الأنثى، فهناك الكثير من الاختبارات الأخرى ومنها:

• اختبار حس الفكاهة عند الرجل: النساء تستخدم حس الفكاهة عند الرجل بطريقة غير واعية لتشتق منه الصفات الأخرى عند الرجل، فالنساء تعتبر الفكاهة دليل ذكاء وإبداع. 

• اختبار الغيرة عند الرجل: الغيرة الزائدة تُعد من مظاهر ضعف الثقة بالنفس وهي ناجمة عن الخوف من المنافسة مع الرجال الآخرين، أعطِ فتاتك مساحة من الحرية ولا تُبدِ مظاهر القلق والارتياب والغيرة من زملائها الشباب في العمل أو الدراسة. 

• اختبار السيطرة عند الرجل: لأن الحياة مليئة بالأحداث المضطربة ولا مكان فيها للضعفاء، ولأن المرأة تبحث عن الرجل القوي القادر والمسيطر في المواقف المضطربة حيال الأزمات، كان لا بدَّ لها من أن تقوم باختبار هذه المقدرة عند الرجل من خلال إحداث موقف، يمكن أن يبعث على التوتر عند الرجل، تستطيع المرأة من خلاله اكتشاف جوانب الضعف في شخصية الرجل. 

• اختبار الذوق عند الرجل في اختياراته: فالمرأة تريد الرجل الانتقائي الذي يختار أشياءه بعناية، هذه الانتقائية تعني اختيار الشيء الأمثل مما هو متاح، حيث إن الكثير من الرجال يستخدمون -على سبيل المثال- أي عطر يقع في أيديهم، يرتدون أي بزة يجدونها في الخزانة، لا يبالون بتناسق الألوان. 

• اختبار الذكاء: وذلك من خلال القدرة على التعامل مع المشاكل وإيجاد الحلول لها.

• اختبار التفاعل الاجتماعي: فالمرأة تريد أن تعرف هل أنت شاب منغلق أم أنك تملك شبكة واسعة من الأصدقاء والعلاقات الاجتماعية؟ ما نوعية أصدقائك ومستوياتهم؟ ما نوعية العلاقة التي تربطك بهم؟

كيف تجري المرأة الاختبارات للرجل؟

● ربما تستكشف المرأة جاهزية الرجل لتلبية احتياجاتها والنجاح في امتحاناتها من خلال وضع أسئلة اختباريه تحمل معاني ضمنية خفية، تستنتج المرأة من خلال الإجابة عليها مكنون الرجل الداخلي.

● وربما تستكشف المرأة ذلك من خلال أحاديث الرجل وتصرفاته، فتلتقط المعنى الذي تمثله كل عبارة يقولها أو تصرف يقوم به، فإذا ما كان الرجل يروي لها عن الأمسية التي قضاها البارحة بصحبة الأهل على شاطئ البحر، والأجواء الشاعرية، ودرجة السعادة التي شعر بها، ومدى عشقه لهذه الأجواء، فإن المرأة تكتشف قدرة هذا الرجل على تأمين هذا الجانب من احتياجاتها وهو الحاجة إلى الرومانسية. 

● وربما تستكشف المرأة شخصية الرجل من خلال تصرف أنثوي اختباري، كالتصرف بشكل عدائي أو متكبر عندما يقترب الرجل منها للتكلم معها والتعارف عليها، كما هو في الاختبار الأول الذي شرحته لكَ سابقًا. 

● وربما من خلال اهتمام النساء الأخريات به وتجمعهم حوله والأحاديث التي تروى عنه تستطيع أن تعرف شخصيته وأحواله.

● وربما من خلال ما تفشيه لغة جسده، وربط ما يقوله الرجل بإيماءات وجهه وجسده.

غرائب الاختبارات الأنثوية: 

○ إن هذه الاختبارات تبقى طوال الحياة حتى بعد الارتباط، فالمرأة تريد أن تتأكد باستمرار أنك مازلت ذلك الرجل الذي اختارته يومًا ما.

○ المرأة تحاول أن تجمع معلومات عنك وتكون على دراية كافية قبل أن ترتبط بك، هذه المعلومات لا تتعلق فقط بوضعك المادي والاجتماعي والصحي ومهاراتك وكفاءتك العلمية والمهنية المختلفة، بل تتعداها إلى محاولة معرفة أعماق شخصيتك وجوانب القوى والضعف فيها. 

○ قد يحدث أن تطلب من امرأة أن تتوسط لك لدى امرأة أخرى، فتقوم هي باختبارك نيابة عن المرأة الأخرى، قبل أن تتوسط لك عندها، والغريب أن هذه الاختبارات تكون -تمامًا- كما لو كنت تتقرب إليها هي لا إلى صديقتها، فقد تتصل بها لتتعرف على أخبار جديدة، فلا ترد على الاتصال الهاتفي، وقد تراها تأتي متأخرة إلى الموعد، رغم أنك تلتقي معها فقط لأجل موضوع التوسط مع صديقتها، لا لأجلها هي، وغيرها مما تعرفه أو تعرفتَ عليه من التصرفات الأنثوية الاختبارية.

○ قد يحدث أن تمطرك المرأة بالاختبارات في لقاء واحد فلا تكاد تنجح في اختبار حتى تجد نفسك أمام اختبار آخر.

○ معظم النساء لديهنَّ صيغ اختبارات فعلية ثابتة وردود خاصة على الرجل الراغب في التقرب منهن يستخدمونها ويتداولونها فيما بينهن، فكل واحدة تعلم صديقتها ما الذي يتوجب عليها القيام به حيال كل موقف من المواقف مع الرجل.

○ إن الاختبارات الأنثوية تجري في الوعي والكثير منها يمكن أن يجري في اللاوعي.
أسباب خوف الشاب من التقرب والتعارف على الفتيات:

● الخوف من إظهار الفتاة مشاعر الانزعاج والاستياء والتذمر.

● الخوف من قيام الفتاة بالشكوى والافتراء عليه فيما لم يقترفه عند الجهات المعنية كالمدرس أو المدير أو حتى الشرطة.

● الخوف من مشاعر الخيبة والإحباط الناجمة عن الصد.

● الخوف من سخرية الآخرين من عودته خائبًا.

● الخوف من تفاعل الفتاة مع محاولته التقرب منها بطريقة عدائية غير لائقة ومهذبة (خاصة إذا ما كان هناك في المكان بعض الشباب ذوي الشهامة العالية والذين ربما يسارعون في التدخل وربما يُقْدِمُون على إهانته أو ضربه).

كيف تتغلب على التمنع أو الصد) الرفض الأنثوي(؟ 

1 ) عنصر أساسي في التغلب على التمنع والصد هو التغلب على أسبابه، فلا يوجد مبرر بأن يكون سبب الصد متعلقًا بك، أنت يجب أن تعالج جميع الأسباب المتعلقة بك أولًا: (المظهر، الثقة بالنفس، السلوك المسيطر، التخلص من مشاعر العوز والحاجة والتصرفات المرتبطة بهم، النجاح في الامتحانات التمهيدية، احترام حاجة الأنثى للألفة والأمان، إتباع الأسس الصحيحة في التعارف والتعامل مع النساء).

2 ) احترام العلاقات، الفتاة التي تملك علاقة يجب إهمالها، بمعنى عدم الضغط عليها لأجل تكوين علاقة عاطفية.

3 ) أن تكون في ذهنك دراية مسبقة أن المرأة التي تريد أن تتعارف عليها ليست هدفًا مؤكدًا، فربما تحقق النجاح أو الفشل، وأنك لست وحدك المسؤول عن الرفض، فهناك الكثير من الأشياء التي لها دور في ذلك، ولأن النساء مختلفات لا يوجد تقنية تعمل على جميع النساء، فمنهنَّ من لا حظ لك معهنَّ إطلاقًا. 

4 ) الإيمان بحق المرأة في إبداء شيء من التمنع والصد، فهناك أسباب منطقية لذلك أحيانًا.

5 ) عدم التشبث بامرأة واحدة ما لم تكن مرتبطًا فعليًّا، تعرف على أكبر عدد من الفتيات، فإن ذلك يخرجك من العوز والحاجة، ويعطيك بدائل ويجعلك انتقائيًّا.

6 ) لكي تنجح يجب أن تكون جاهزًا للتغلب على الخوف من الصد، فلا يوجد تعارف بدون خطر الصد أو الرفض، تقبّل الصد بروح رياضية، فالعالم مليء بالنساء الجميلات العزباوات.

7 ) عدم السماح للتمنع والصد أن يؤثر عليك أو ينال منك ويحبطك مهما كانت صورته، واعتبار أن الأمر أقل من عادي، والتصرف بهدوء أعصاب تام، تعاملك مع الموقف بطريقة متعجرفة أو جبانة يجعلك رجلًا من الدرجة الثانية في نظر المرأة، وبالمثل يكون عندما تتقرب من المرأة بحذر مفرط في الخوف أو أن تتصرف بشكل غريب، أنتَ يجب أن تبقى مسترخيًا.

8 ) عدم التأثر بتظاهر المرأة بالتململ وعدم الاهتمام، الانشغال، الانزعاج، هذه التظاهرات يجب أن لا تعيقك عن بلوغ الهدف، تقرب من الفتاة وبادر بالحديث معها بأسلوب لطيف، تقبل كل الأخطار، كل امرأة تعجبك تكون فرصة شريك للحياة، لكن القليل من الفرص يقود إلى نتيجة؛ أنتَ يجب أن تستخدم الكثير من الفرص لكي تحصل في النهاية على النتيجة المرجوة. 

9 ) الانتباه إلى لغة جسد الفتاة وطريقة تصرفها لتعرف ماذا تعني كلمة (لا) وماذا تعني كلمة (نعم)، فالكثير من الشباب يهملون هذا الجانب ويقعون في الخطأ وهو أن يعتبروا كلمة (لا) عند المرأة تعني بالفعل (لا). (لا) ربما تعني ماذا تريد مني؟ أعطني سببًا مقبولا، لماذا يجب علي أن أتحدث معك؟ بالمثل فإن كلمة (نعم) عند المرأة قد يكون معناها (لا) ولكنها تجد صعوبة في أن تقول (لا) لأنها لا تريد أن تجرحك.

10 ) عدم الأخذ بذرائع المرأة التي تستعملها في التمنع والصد على محمل الجِدِّ، فمعظمها حجج واهية لا وجود لها، لا تدعها تؤثر عليك، فالذريعة بعدم وجود وقت -على سبيل المثال- ذريعة واهية.

11 ) تتميز النساء باستخدامهن للأسلوب غير المباشر في الكلام وعادة ما تحوي عبارات النساء معنًى ضمنيًّا أو خفيًّا، يتوجب على الرجل استنتاجه لتلبية مطلب المرأة، وكأمثلة على ذلك: 	

عندما تقول المرأة:

منذ زمان لم نشم الهواء. فهي تعني: ما رأيك أن تأخذنا في نزهة؟
منذ متى لم ترَ أهلك؟ فهي تعني: ما رأيك أن نذهب لزيارة أهلك؟
	
إن معرفة المعنى الضمني أو الخفي للعبارة، كذلك الهدف الضمني أو الخفي للتصرف الذي تقوم به المرأة سيساعدك على معرفة أي نوع من الحاجات الداخلية أو الاختبارات الأنثوية يمثل ذلك، وسيساعد على الرد والتصرف بشكل صحيح ولنأخذ بعض الأمثلة على ذلك:

مثال 1:
العبارة الأنثوية: ابحث عن فتاة أخرى.
(وهي من أشهر العبارات التي تقولها الفتاة للشاب عندما يتقرب منها بغية التعرف عليها) 

المعنى الضمني: إذا كنت تبحث عن الجنس فابحث عن فتاة أخرى، أنا لست الفتاة التي تبحث عنها. 

الهدف الضمني: معرفة المنظور الذي تراها به، هل تنظر إليها كعاهرة تريد إمضاء بعض الوقت معها، أم أنك ترى فيها فتاة أحلامك وشريكة المستقبل. 

الحاجة: الحاجة إلى الأمان العاطفي.
 
التعامل الصحيح: أن تقول لها: أنا لم أتقرب منكِ للتعارف عليكِ لمجرد أنكِ فتاة، ولكن لأنني شعرت بأنكِ يمكن أن تعني لي الكثير في حياتي ومستقبلي، لا أحد يعرف ما يخبئ القدر، ربما تكونين أنتِ الفتاة التي أبحث عنها لتكون شريكة حياتي. 

مثال 2: 

التصرف الأنثوي: التأخر الملحوظ والمتعمد في القدوم إلى الموعد.
(وهو من التصرفات الشائعة والذي تقوم به الكثير من الفتيات عند المواعدة)

الهدف الضمني: قد يكون الهدف من هذا التصرف إعطاء المرأة الشعور بأهميتها، وقد يكون الهدف معرفة كيف  سيتصرف الرجل في حال تأخرها.

الاختبار: اختبار السيطرة عند الرجل. 

التعامل الصحيح: أيَّ كان الهدف يجب أن تبقى هادئًا مسترخيًا وأن لا تبدي أيًّا من   مظاهر القلق والتوتر بسبب تأخرها.

12 ) أنتَ يجب أن لا تأخذ تصرف المرأة على محمل الجد، بل يجب أن تنتبه إلى أي نوع من المخاوف أو الحاجات الداخلية يمثل هذا التصرف (الحاجة إلى الأمان من الاعتداءات التي يمكن أن يمثلها الرجل الغريب، الحاجة إلى الأمان العاطفي، الحاجة إلى الأمان الاجتماعي، الحاجة إلى المحادثة.....) وربما كنوع من الاختبارات ليتسنى لها أن تقيمك بالشكل الأفضل ومعرفة أي نوع من الرجال أنت.

