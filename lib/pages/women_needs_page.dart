5. الحاجة إلى تعابير الحب

المرأة بحاجة أن تشعر أنها محبوبة، ولا بد لها من أن تسمع عبارات الحب من الرجل باستمرار لتتأكد أنها بالفعل محبوبة، وكلما كثرت وتنوعت عبارات الحب كلما شعرت المرأة بالارتياح والرضا، فالقليل من عبارات الحب لا يمكن أن تشبع حاجة المرأة للحب، كذلك الأمر يجب أن تتوافق تعابير الحب كما هو في الإطراء والغزل مع لغة جسدك، بمعنى آخر أعطِ المشاعر للكلمات التي تقولها من خلال لغة الجسد، تمرن على ذلك من خلال متابعة أفلام الحب والرومانسية، فمعظم النساء تحلم بالرجل الذي يقول لهنَّ تلك العبارات والجمل المقتبسة من الأفلام والمسلسلات التي يحبونها، خاصة إذا ما قمت ببعض التعديلات الإبداعية عليها، لتعطيها المزيد من الجمال والعذوبة.

ومن عبارات الحب

- أنتِ معنى حياتي، بدونكِ لا معنى لحياتي.
- أنا لم أعد أستطيع أن أخفي مشاعري تجاهكِ، أنا يجب أن أقول لكِ: إنني أحبكِ.
- قبل أن أستودعكِ وأقول لكِ مع السلامة، صدقيني إذا قلت لكِ: إنني أحبكِ.
- أنا لا أستطيع أن أتخيل نفسي من دونكِ.
- أتمنى أن نبقى لبعضنا دائمًا.
- أنتِ استطعت أن تملكي مفاتيح قلبي.
- فقط التفكير بكِ يسحرني ويرسم الابتسامة على وجهي.
- أنتِ المرأة الأولى والأخيرة التي أقع فعلًا في حبها.

