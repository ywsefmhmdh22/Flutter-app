2. الحاجة إلى الألفة

حتى تتقرب من الفتاة أكثر فأكثر لا بدَّ من حدوث الألفة أولًا، فالفتاة تحتاج وقتًا أطول من الشاب لتطوير الاهتمام به، وبناء المشاعر العميقة تجاهه، فلن توافق الفتاة أن تصادقك أو تخرج معك سريعًا، لا بدَّ من أن تعرفك جيدًا حتى تنجذب إليك.

محاولة التطور السريع في العلاقة سيفضي إلى الفشل، فالفتيات لا تنجذب للغرباء بسرعة وتخشى من تكوين العلاقات معهم، يجب عليك أن تصبح مألوفًا لها أولًا حتى تنجذب إليك، لا تستعجل الأمور، كن صبورًا، فبناء العلاقة مع الفتاة يحتاج إلى الوقت ولا يأتي بيوم وليلة.

هذه الألفة تتعلق بعدد المرات التي تراك فيها الفتاة والتي تتحدث فيها إليها، فالكلام هو ما يحدث الألفة، وكلما كثرت المواضيع وتشعبت كلما زادت الألفة، كما أن اللقاء مع الفتاة مصادفة في أماكن مختلفة يعزز الشعور بالألفة والمودة، فإذا كنت تلتقي الفتاة في العمل -على سبيل المثال- وتتحدث معها بصورة متكررة وحدث وأن التقيت بها صدفة في السوق وبادرت بالتحية والتحدث معها، فإنك ستلاحظ إشراقه ترتسم على وجهها خلال اللقاء وأنها أصبحت أكثر انفتاحًا ومودة تجاهك، وبأن الألفة بينكما بدأت تتصاعد.

في بداية العلاقة يتوجب عليك أن تكون أكثر ندرة في حياة الفتاة، ولكن دون مبالغة؛ لأن ذلك يجعلك تفقدها، اجعل الأمر متوازنًا، فرغم أن عدد مرات التحدث واللقاء مع الفتاة عامل أساسي في بناء الألفة، إلا أنه يجب أن يتم ضمن إطار زمني مقبول.

كون في البداية معرفة عادية معها، لا تتطرق إلى الرغبة في تبادل أرقام الهواتف أو الخروج معها أو العلاقة أو الغزل أو الحب بصورة مبكرة، قبل تَكون درجة كافية من الألفة بينكما، تدرج شيئًا فشيئًا في التودد إليها، عليك أن تبني الثقة بينكما أولًا، الانتقال من العام إلى الخاص والابتعاد تدريجيًّا عن مجرد كونكما مجرد معرفة عادية، ربما يرافق هذا التحول شيء من رد الفعل عندها، عليك أن تقابله بنوع من المرونة، ولا تتوقف عند هذا الحد فالأمور ستعود إلى طبيعتها.
