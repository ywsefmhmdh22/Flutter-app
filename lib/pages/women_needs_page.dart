4. الحاجة إلى لفت الانتباه إلى مظهرها وزينتها

كافة النساء ودون استثناء تنجذب للرجل الذي يقوم بمدحها والإطراء عليها سواء كان ذلك بطريقة مباشرة أو غير مباشرة، وكيف لا وهي مخلوق جمالي وجمالها مصدر جاذبيتها للرجل، والمهم هنا أن يكون الإطراء أو الغزل نابعًا عن مشاعر حقيقية بالإعجاب، وأن تكون عبارات الغزل متوافقة مع لغة جسد الرجل، وما يتوجب على الرجل فعله هو أن يبحث عن كل شيء جميل ومميز في المرأة، عن كل تفصيل، خاصة إذا ما كانت هناك أشياء جميلة عندها تميزها عن الأخريات، كذلك أيضًا يؤثر الإطراء بشكل أكبر عندما يقوم الرجل بإطراء شيء جميل ومميز عند المرأة لم ينتبه أو يتطرق إليه رجل آخر من قبل، ومن الأشياء التي يمكن للرجل أن يقوم بإطراء المرأة عليها: جمال الوجه، جمال البشرة، جمال القامة، جمال العيون، جمال الشعر، جمال الأسنان، جمال الابتسامة، عذوبة الصوت..... كذلك على الرجل أن ينتبه إلى أي تغيير في مظهر الفتاة، كارتداء ثوب جديد وأنيق، قصة أو تسريحة شعر جديدة، عطر جديد، إكسسوارات جديدة ..... بالإضافة إلى استكشاف العناصر الجمالية في المظهر على الرجل استكشاف عناصر الجمال في روحها وشخصيتها والأشياء التي تقوم بها وكل ما تلامسه أصابعها، مثال على ذلك: خفة الدم، طيبة القلب، سرعة البديهة، قوة الذاكرة، تنسيق الزهور، مذاق العصير الذي أعدته، الأشياء التي اشترتها........ والإطراء عليها، فهي تحب أن تسمع ذلك بصورة لا تصدق، وما يتوجب على الرجل أن ينتبه إليه عند إلقاء عبارات الإطراء هو مراقبة رد فعل المرأة ولغة جسدها، ابتسامتها، تغير لون وجهها، فرحها وإشراقها.

انتهز كل فرصة للإطراء، فالمرأة تحب أن تسمع عبارات الإطراء باستمرار، الإطراء والغزل يؤثران بشكل أفضل عندما يقوم الرجل بتبديل الحديث على الفور بعد إلقائه عبارة الإطراء أو المديح، كذلك يفضل أن لا يأتي الإطراء والغزل منفردًا، بل عرضيًّا أثناء الحديث، ومنح المجاملات المناسبة في الوقت المناسب (الكلمة المناسبة في الوقت المناسب) حاول أن تقوم بالإطراء عليها بما ينسجم مع المحادثة مرة أو مرتين، لا تبالغ ولا تتملق ولا تستخدم الصورة النمطية، كن مبدعًا.

كما أن عبارات الغزل يجب أن تكون صادقة وواقعية، وليست مبالغة جدًّا أو لا وجود لها في الواقع، يجب كذلك أيضًا أن لا تحمل دلالة سلبية ضمنية، كأن تقول لها: هذا الثوب يجعلكِ جذابة ومثيرة، فالمعنى الضمني للعبارة هو أنها من دون هذا الثوب تبدو غير جذابة ومثيرة، والعبارة الصحية أن تقول لها: هذا الثوب يجعلكِ أكثر جاذبية وإثارة، أو إن هذا الثوب أبرز ما كان خفيًا من جاذبيتك.

ومن الملاحظات الأخرى عندما تغازل فتاة، وتقول لها إنها جميلة مثلًا، ترد عليك متظاهرة أنها شخصيًّا لا تكون راضية تمامًا عن جمالها، وأنها تجد نفسها نحيلة مثلًا، أو أن التجاعيد بدأت تظهر على وجهها. هنا يتوجب عليك أن تؤكد لها من جديد أنك تجد كل شيء عندها تامًّا ومميزًا، أو أنك تحب هذه السمة عندها والتي لا تكون هي عنها راضية.

ومن عبارات الغزل

- أنتِ تبدين جذابةً جدًّا.
- أنتِ كأشعة الشمس دافئة وحنونة.
- أنتِ أحلى من أحلى شيء موجود على سطح الأرض.
- لا وجود لمغناطيس يملك قوة جاذبية كقوة جاذبيتكِ.
- أنتِ جميلةً تمامًا كما أنتِ.
- أنا أكثر من محظوظ لأنني إلى جواركِ.
- أنتِ تملكين وجهًا محببًا جدًّا.
- وجهكِ يبدو كنجمة مضيئة في السماء.
- أنا لم أسمع في حياتي صوتًا أجمل من صوتكِ.
- لا يمكن لمخلوق أن يقاوم ابتسامتكِ الرائعة هذه.
- أنا لم أرَ ابتسامة أجمل من ابتسامتكِ.
- أتمنى أن يكون هذا اليوم جميلًا كابتسامتكِ.
- أنا أنظر عميقًا في عينيكِ لأتعلم أن أرى كل شيء جميلًا.
- لقد كنتِ هذه الليلة أكثر من رائعة.
- أنتِ أجمل امرأة في هذا العالم.
- تسريحة شعركِ رائعة.
- أنتِ تملكين ذوقًا مميزًا في اختيار الألوان والألبسة.

