7. الحاجة إلى الجنس

الجنس عند معظم النساء يعني شيئًا مختلفًا تمامًا عما هو عند معظم الرجال، الجنس عند الرجل يعني الشهوة والإثارة، أما عند المرأة فهو عمل فني متكامل، الجنس عند المرأة يعني الخيال، التحفيز الروحي، التقارب، التخلي عن الذات، أن تكون مرغوبة، والكثير من المشاعر المعقدة، فالنساء يردن اقتسام الألفة مع الرجل، المرأة تحب أن تشعر أنها مرغوبة أو مشتهاة، لكن هذا يجب أن يكون حولها هي وليس فقط حول الجنس معها، فالجنس عند المرأة يكون مرتبطًا بالحاجات العاطفية الأخرى، كالحاجة إلى المحادثة والحاجة إلى الحب والغزل والرومانسية والألفة والأمان.

النساء لا تفكر بشكل مستمر بالجنس مثل الرجال، الرجل يفكر بالجنس أكثر كلما كان يحصل عليه أقل، المرأة تفكر بالجنس أقل كلما كانت تحصل عليه أقل، في بعض الأحيان يتوجب على الرجل أن يوقظ هذه الحاجة عند المرأة.

معظم النساء يبدأ التفكير بالجنس عندهن مع التفكير بالمواقف الرومانسية، فالنساء تحب الكلام الرقيق، المداعبة واللمس والاحتضان والقبلات العميقة والتي تؤدي إلى إفراز هرمون الأوكسي توسين بكميات كبيرة والذي يعطيها المزيد من الرغبة بالتقارب والارتباط؛ لذا يدعى هذا الهرمون بهرمون الدلال والمحبة.

الرغبة الجنسية والإشباع الجنسي عند المرأة يتعلق بصورة كبيرة أيضًا بالجو المحيط. [عدم وجود ضجة أو ما يبعث على القلق والتوتر في المكان، الإضاءة الخافتة، إغلاق الستائر، الموسيقى الرومانسية الهادئة، نظافة المكان، السرير الواسع والمريح، نظافة المخدات ومفارش السرير ورائحتها العطرة، رائحة الغرفة العطرة والمنعشة، والتي تلعب جميعها دورًا كبيرًا عند المرأة بالشعور بالارتياح والاستمتاع بالعملية الجنسية].

