3. الحاجة إلى الاستماع والتعاطف

لا يدرك الكثير من الرجال ما للتحدث عن النساء من أهمية، فقد تفوق أهميته أهمية النجاحات وتحقيق الأهداف، وجدير بنا أن نعلم أن النساء تحتاج إلى الاستماع والتعاطف، كذلك الأمر فإن النساء تحب أن تشاركك أفكارها كعلامة على التعلق والارتباط، وأكثر ما يضايقهنَّ هو عدم استماع الرجال إليهنَّ، فالمرأة تبحث عمن يتعاطف معها ويستمع إليها، لكي تشعر بالتحسن، خاصة عندما تكون واقعة تحت تأثير ضغوط مشاكلها اليومية، والمرأة بعكس الرجل لا تعتبر وجود المشاكل عندها أمرًا يعيبها، بل إنها تجد الراحة في الحديث عنها وبصورة تفصيلية دقيقة.

هذا وللحديث عند المرأة ما يميزه عن الحديث عند الرجل، فعندما تتحدث المرأة عن موضوع أو مشكلة ما، فإنها تتوقف لبرهة من الزمن، ثم تعود للحديث منتقلة إلى موضوع آخر يكاد يكون غير مرتبط منطقيًّا بالموضوع السابق.

ولكي تعطي المرأة الموضوع الذي تتحدث عنه شيئًا من الإثارة فإنها تعمل على ترك القلق يتعاظم في قصتها من خلال التحدث عن المشاكل بطريقة مثيرة للإحباط وخيبة الأمل.

وفي حين أن الرجل يعمد عندما يقع تحت ضغط عدة مشاكل إلى تناسيها لبعض الوقت والانشغال بالمشكلة الأهم، فإن المرأة تغرق في المشاكل دفعة واحدة ودون التركيز على واحدة منها، فترى المرأة تتحدث عن مشاكل في الماضي والحاضر والمستقبل وعن مشاكل محتملة وأخرى لا وجود لها، وكلما كثر حديثها واستكشافها لما يزعجها، كلما كان الشعور بالتحسن عندها أكبر.

على العموم تكره النساء الصمت وتسيء تفسيره، فعندما يصبح الرجل فجأة صامتًا، تصبح المرأة على الفور غير آمنة وتنتابها مشاعر القلق والتوتر، وتبدأ مخيلتها في تصور كل ما هو سيّئ متوقعًا، ويبدأ الشك يتغلغل في أعماقها وتبدأ تحدث نفسها: ربما هناك امرأة أخرى في حياته، ربما هناك شيء يخفيه عنّي؟!
الأشياء التي يتوجب عليك فعلها لأجل ذلك:

● تشجيعها على الحديث عن ذاتها، مشاكلها، اهتماماتها، إنجازاتها وآرائها، ضع أسئلة محددة عن مجريات يومها توحي بالاهتمام بشؤونها، مثال: كيف كان الامتحان اليوم؟ أو كيف كانت إجاباتكِ؟ ومتى ستحصلين على النتيجة؟

● ومن الأفضل قبل أن تسألها عن مجريات يومها أن تلاحظ كيف تشعر من خلال قراءة لغة جسدها، فإذا ما كانت متضايقة، يمكنك سؤالها: يبدو أنك متضايقة، هل حدث شيء معكِ؟ ثم يأتي السؤال كيف كان يومكِ؟

● أن تمتلك الإيمان بأن للمرأة الحق في أن تشعر بالذي تشعر به، فإذا ما بدا عليها أنها متضايقة -على سبيل المثال- فما عليك إلا أن تصادق على مشاعرها وتبذل لها شيئا من التعاطف بدل تقديم الحلول، والذي يكون من الأخطاء الكبيرة والتي يُقدم عليها الكثير من الرجال.

● تعود على الإصغاء وطرح الأسئلة التي توحي بالاهتمام بشؤونها، حافظ على اتصال العين.

● لا تدع انتباهك يتشتت بأي شيء خلال حديثها، كقراءة الصحف والرد على الهاتف أو النظر والتدقيق في الساعة.

● طمئنها بأنك تنصت لها، من خلال الإيماءات، كهز الرأس، أو بعض العبارات مثل: صحيح، معكِ حق، ربما يكون الأمر كذلك.

● استخدم الألفاظ المعبرة عن المشاعر في حديثك معها مثل: أنا مسرور، أنا متحمس، أنا أشعر...

● إذا ما كنت بحاجة إلى الاختلاء بنفسك، فأعلمها بذلك وقل لها: إنك تحتاج بعض الوقت للتفكير، لأجل بعض الأشياء التي يجب أن تقوم بإعدادها.
