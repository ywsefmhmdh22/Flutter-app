### 5. الهدايا والمفاجآت الجميلة
المفاجآت الجميلة تترك أثرًا طيبًا وقويًا على نفس الإنسان، أجمل هذه المفاجآت هي التي تأتي من دون انتظار أو توقع، فما أجمل أن يفاجئ المرء من يحب بما يحب دون ترتيب أو موعد، هذا الأثر الجميل الذي تتركه المفاجآت الجميلة لا ينتهي بانتهاء وقته، بل إنه يبقى في ذاكرة الإنسان، وفي تذكره تقوية لمشاعر الحب والتقارب، لأنك بهذا تشعر الشخص الآخر بقيمته ومنزلته لديك، وكم هو من شعور إنساني رائع وعظيم، والسعادة التي يشعر بها الإنسان عندما يشتري هدية لمن يحب ويرى الفرحة في بريق عينيه لا يمكن أن تقدر بثمن، ولكننا غالبًا ما نشعر بالحيرة تجاه اختيار هذه الهدايا ولأجل ذلك ننصح بالآتي:

انتبه إلى الإشارات والتلميحات البسيطة:
انتبه: خلال المحادثة تعطيك المرأة إشارات عما هو مهم لديها ولماذا، ما ترويه لك المرأة يمكن أن يكون أرضية لمفاجأة رومانسية، هي تحب مطربًا أو أغنية ما، نوعًا من الكتب أو القصص، يمكنك أن تحضر لها مفاجأة، هدية صغيرة عبارة عن كاسيت للمطرب أو الأغنية التي تحبها، كتاب أو قصة من القصص التي تهوى قراءتها.

بعض الأشياء الجميلة التي يمكن أن تكون هدية لفتاة أحلامك:
الزهور، علبة شيكولاتة صغيرة، الاكسسوارات، العطور ومستحضرات التجميل، كاسيتات الأغاني، الكتب المسلية والروايات الرومانسية، بطاقة دعوة لحضور مسرحية أو حفلة موسيقية أو غنائية، بطاقات التهنئة بالمناسبات والأعياد، قسيمة الهدايا (وهي عبارة عن قسيمة بمبلغ مادي معين على شكل كارت يتم شراؤها من إحدى المحال التجارية، ومن خلالها يستطيع الشخص الحاصل عليها أن يذهب إلى هذا المحل وشراء ما يريد بقيمة المبلغ الذي دفعته أنت، وهي طريقة منتشرة في الكثير من دول العالم).

اكتب بعض العبارات الجميلة على بطاقة صغيرة مرفقة مع الهدية:
هذه العبارات يمكن أن تكون من إبداعك الشخصي وربما أبيات من الشعر والتي يمكن أن تبقى في ذاكرة المرأة.

تجنب الهدايا مرتفعة الثمن:
شراء الهدايا مرتفعة الثمن يعطي المرأة الشعور بأنك تريد أن تشتريها بأموالك، والنساء تقيم الهدايا بقيمتها المعنوية لا بقيمتها المادية، سواء اشتريت لها عقدًا من الماس أو علبة شيكولاتة صغيرة ستحظى بنفس النتيجة.

المفاجآت لا تعني فقط الهدايا والأشياء المادية:
هناك الكثير من الأشياء غير المادية والتي يمكن أن تحضرها كمفاجأة لفتاة أحلامك، فإذا كانت الفتاة من قومية أخرى - على سبيل المثال- الإسبانية، فكم يكون رائعًا وجميلًا حفظ بعض المفردات والعبارات بلغتها ومفاجأتها بها مثل: مرحبًا، كيف حالكِ؟ كيف كان نهاركِ؟ باللغة الإسبانية.

