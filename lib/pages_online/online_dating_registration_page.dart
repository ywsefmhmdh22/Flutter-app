*5. إنشاء ملف التعريف*

* تجنب الأخطاء اللغوية والإملائية وراعِ الفواصل والنقاط وعلامات الاستفهام، فالإهمال في الكتابة يُعتبر كالإهمال في النظافة الشخصية.
* لا تستخدم الكتابة بالأحرف الكبيرة إذا كنت تكتب بلغة أجنبية كالإنجليزية والفرنسية والألمانية وغيرها.
* ملف التعريف يجب أن يُشير إلى أنك *رجل فكاهي، حساس، اجتماعي، والأهم وفيّ للشريك*.
* محتوى ملف التعريف يجب أن يوحي بأنك إنسان ناجح وافر الحظ مع النساء، أنت تختار الفتاة عندما توجد عوامل نجاح مقصدك فيها، هذا يعني أنك فريد من نوعك، تمتلك السيادة، الثقة بالنفس، والجدارة بالثقة.
* تجنب الندب والنحيب: "أريد أن أتخلص من العزوبة والوحدة، لم أعد أُطيق أن أجلس وحيدًا على الفطور" (كلما زادت الحاجة واليأس، كلما قل النجاح).
* لا تُبين أن هدفك الوحيد في الحياة هو الحصول على فتاة أحلامك.
* لا يُحبذ أن تملأ جميع البيانات التي يطلبها منك الموقع حول نفسك، هذا سيعطي المرأة إمكانية تفحص كل كلمة في ملف تعريفك واستخلاص المعاني الخفية، فالمرأة لا تقرأ الكلمات بل ما بين السطور، فوجود عبارات تحمل معاني ضمنية سلبية يقودها إلى تجاهلك والانتقال إلى الشخص الآخر على قائمة المعجبين. ما يتوجب عليك هو كتابة عبارة في ملف تعريفك: "*من يريد أن يعرف المزيد عني بإمكانه ذلك بكل سرور عبر مراسلتي*".
* عندما يطلب منك أن تكتب نصًّا في ملف التعريف، حاول أن تكتب ما لا يقل عن *خمسة أسطر*، فالقليل لا يكون كافيًا.

