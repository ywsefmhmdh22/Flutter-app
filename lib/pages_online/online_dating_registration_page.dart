*3. اختيار الصور*

الصورة يجب أن تُعطي انطباعًا جيدًا عنك وتوحي بمظهر أنيق وعناية كبيرة بالذات، يجب أن توحي بالسعادة والسرور، بالنشاط والحيوية والطاقة والثقة بالنفس. ولذلك، يتوجب عليك الاستفادة من النصائح التالية:

* ابحث في مواقع الإنترنت وامتلك شيئًا من الدراية أو الخبرة فيما يتعلق بفن التقاط الصور والتعامل معها، فالصور الجذابة وغير الاعتيادية تلفت الانتباه أكثر وتُثير الحماس للتواصل مع أصحابها مقارنة بالصور النمطية المملة والمكررة.
* اختر شخصًا من معارفك أو أصدقائك يُجيد التقاط الصور. المصور يجب أن يُعطي توجيهات للشخص الذي يقوم بتصويره مثل: اقترب قليلًا، ارفع رأسك، ارفع أكتافك، أبرز صدرك، اشفط بطنك، ابتسم، استدر قليلًا، أخرج يدك من جيبك، لا تُغلق عينك، أصلح هندامك، امسح العرق عن وجهك، أعد تصفيف شعرك، وغيرها من التوجيهات اللازمة لأجل صورة ناجحة.
* حضر نفسك للتصوير وارتدِ ثيابًا جديدة نظيفة أنيقة تناسبك.
* اختر أماكن التقاط الصور، كـ*الطبيعة* والأماكن *الأثرية* والأماكن *الغريبة*.
* خذ صورًا تمثل قضاء أوقات جميلة مثل نزهة أو قضاء عطلة أو غيرها.
* إذا كنت تملك هواية معينة كالعزف على الآلات الموسيقية، السباحة، كرة القدم... إلخ، فخذ لك بعض الصور وأنت تُمارس هواياتك.
* كن لوحدك في الصورة الرئيسية (الأولى) وفي عدد من الصور الأخرى.
* *ابتسم في الصورة*، فالنساء تبحث عن الرجل الذي يملك المرح ويصنعه.
* لا تُخبئ نفسك في الصورة خلف قبعة أو نظارة شمسية أو غيرها، فالفتاة تريد أن ترى وجهك الحقيقي، ووضع اليد على الفم أو الأنف يوحي بأنك تُخفي شيئًا ما في وجهك.
* التقط الصور بكاميرا عالية الجودة.
* اختبر جودة الصورة، خذ عدة لقطات واختبر معايير التحكم بالصورة كالضوء والظل وغيرها.
* لا تأخذ صورًا من مسافات بعيدة.
* تجنب تنزيل الصور التي تحوي عيوبًا كالبقع الضوئية وغيرها من البقع في موقع التسجيل للتعارف.
* لا تستخدم صورة جواز السفر، فهي توحي بأنك لا تخرج من البيت.
* الصورة المأخوذة باليد توحي بأنك وحيد وليس لديك علاقات اجتماعية.
* اختر صورًا حديثة لك، فالفتاة تريد أن تعرف شكلك الآن وليس قبل 5 أو 10 سنوات.
* اختر صورًا لا تحوي عيوبًا تقنية كالعيون الحمر واللمعان وغيرها.
* اختر مقاييس الصورة بعناية، فمعظم مواقع التعارف تطلب صورًا بقياسات معيارية محددة، فالصور الكبيرة أو الصغيرة يمكن أن تظهر بشكل سيئ عند التحميل.
* عالج المشاكل التقنية في الصورة، يوجد الكثير من المراكز والمواقع المتخصصة.
* تجنب تنزيل الصور التي تُحط من مستواك.
* الصورة التي توحي بالرفاهية، كالصورة في منزل فخم أو سيارة فخمة، تزيد من جاذبية الشخص. ومن تلك التي لا تبحث عن الرفاهية، ولكن صاحب الملك تربطه علاقة حميمية بملكه، فالصورة بجانب السيارة لا توحي بملكية السيارة، فصاحب السيارة يجلس خلف المقود أو يستند إلى سيارته وهي مفتوحة الأبواب، أو على الأقل يقف بجانبها وهو يحمل بيده مفاتيح السيارة.
* استشر الفتيات من معارفك في اختيار الصور الأكثر جاذبية.
* اختبر درجة تقييم صورتك على الإنترنت، فهناك الكثير من المواقع التي تقدم هذه الخدمة.
* نوّع الألبسة في الصور، فمن غير اللائق تنزيل عدة صور بنفس اللباس.
* نوّع الوضعيات: صورة وأنت تركض، صورة وأنت تسبح، صورة وأنت تجلس إلى الطعام، صورة وأنت تقرأ الجريدة...
* نوّع أماكن التقاط الصور، فإن تنزيل صور متعددة ملتقطة في مكان واحد لا يبدو جذابًا.
